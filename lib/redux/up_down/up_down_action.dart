class ShowUpButtonAction {
  final bool showUpButton;
  ShowUpButtonAction({
    required this.showUpButton,
  });

  @override
  String toString() => 'ShowUpButtonAction(showUpButton: $showUpButton)';
}

class ShowDownButtonAction {
